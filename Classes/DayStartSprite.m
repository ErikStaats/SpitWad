/* Self import. */
#import "DayStartSprite.h"


/* *****************************************************************************
 *******************************************************************************
 *
 * Day start sprite class implementation.
 *
 *******************************************************************************
 ******************************************************************************/

@implementation DayStartSprite

/* *****************************************************************************
 *
 * Day start sprite NSObject implementation.
 *
 ******************************************************************************/

/*
 * init
 *
 *   This function initializes the day start sprite.
 */

- (id)init
{
    /* Initialize the super class with the day start view. */
    self = [super initWithNibName:@"DayStart"
                  bundle:[NSBundle mainBundle]];
    if (!self)
        return nil;

    /* Set the sprite views. */
    self.spriteView = self.view;

    return self;
}


@end

